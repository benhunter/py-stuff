def draw(n):
    width = n * 3

    for i in range(1, n // 2 + 1):
        # print(i)
        s = '.|.' * ((i * 2) - 1)
        print(s.center(width, '-'))

    print('WELCOME'.center(width, '-'))

    for i in range(n // 2, 0, -1):
        # print(i)
        s = '.|.' * ((i * 2) - 1)
        print(s.center(width, '-'))


draw(7)
print()
draw(9)
print()
